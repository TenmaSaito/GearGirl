//=========================================================================================
// 空の初期化処理
//=========================================================================================
void InitMesh(void)
{
	// Sphere
	InitMeshSphere();

	// Cylinder
	InitMeshCylinder();

	SetMeshSphere(vec3(0.0f, 120.0f, 0.0f), D3DXVECTOR3_NULL, 1000.0f, 16, 32, true, true, 0);
}

//=========================================================================================
// 空の終了処理
//=========================================================================================
void UninitMesh(void)
{
	// Sphere
	UninitMeshSphere();

	// Cylinder
	UninitMeshCylinder();

}

//=========================================================================================
// 空の更新処理
//=========================================================================================
void UpdateMesh(void)
{
	// Sphere
	UpdateMeshSphere();

	// Cylinder
	UpdateMeshCylinder();

}

//=========================================================================================
// 空の描画処理
//=========================================================================================
void DrawMesh(void)
{
	// Sphere
	DrawMeshSphere();

	// Cylinder
	DrawMeshCylinder();

}
