//==================================================================================
// --- 初期化 ---
//==================================================================================
void InitTitle(void)
{
	/*** ライトの初期化 ***/
	InitLight();

	/*** 選択肢の初期化 ***/
	InitTitleSelect();

	/*** カメラの初期化 ***/
	InitTCamera();

	// カメラの追加
	AddTCamera(CParamVector::V3NULL, CParamVector::V3NULL, 100.0f);
}

//==================================================================================
// --- 終了 ---
//==================================================================================
void UninitTitle(void)
{
	/*** ライトの終了 ***/
	UninitLight();

	/*** 選択肢の終了 ***/
	UninitTitleSelect();

	/*** カメラの終了 ***/
	UninitTCamera();
}

//==================================================================================
// --- 更新 ---
//==================================================================================
void UpdateTitle(void)
{
	/*** ライトの更新 ***/
	UpdateLight();

	/*** 選択肢の更新 ***/
	UpdateTitleSelect();

	/*** カメラの更新 ***/
	UpdateTCamera();
}

//==================================================================================
// --- 描画 ---
//==================================================================================
void DrawTitle(void)
{
	/*** カメラの設置 ***/
	SetTCamera();

	/*** 選択肢の描画 ***/
	DrawTitleSelect();
}