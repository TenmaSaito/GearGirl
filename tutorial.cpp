//==================================================================================
// --- 初期化 ---
//==================================================================================
void InitTutorial(void)
{
	// テクスチャの初期化
	InitTexture();

	// 2Dポリゴンの初期化
	Init2DPolygon();

	// ダイアログの初期化
	InitDialog();

	// ダイアログの設置
	SetDialog();
}

//==================================================================================
// --- 終了 ---
//==================================================================================
void UninitTutorial(void)
{
	// テクスチャの破棄
	UninitTexture();

	// 2Dポリゴンの破棄
	Uninit2DPolygon();

	// ダイアログの破棄
	UninitDialog();
}

//==================================================================================
// --- 更新 ---
//==================================================================================
void UpdateTutorial(void)
{
	// 2Dポリゴンの更新
	Update2DPolygon();

	// ダイアログの更新
	UpdateDialog();
}

//==================================================================================
// --- 描画 ---
//==================================================================================
void DrawTutorial(void)
{
	// 2Dポリゴンの描画
	Draw2DPolygon();

	DrawDialog();
}