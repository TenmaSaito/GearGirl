IDX_2DPOLYGON g_nIdxHResultPolygon[TEX_HAPPYEND];	// 2Dポリゴンのインデックスを保管

//==================================================================================
// --- 初期化 ---
//==================================================================================
void InitHappyEnd(void)
{
	int Tex;

	LoadTexture("data/TEXTURE/resultBG.png", &Tex);
	g_nIdxHResultPolygon[0] = Set2DPolygon(D3DXVECTOR3(SCREEN_WIDTH / 2, SCREEN_HEIGHT / 2, 0.0f), VECNULL, D3DXVECTOR2(1280, 720), Tex, DEF_COL);

	LoadTexture("data/TEXTURE/Clockneedle.png", &Tex);
	g_nIdxHResultPolygon[1] = Set2DPolygon(D3DXVECTOR3(SCREEN_WIDTH / 2, SCREEN_HEIGHT / 2, 0.0f), VECNULL, D3DXVECTOR2(100, 100), Tex, DEF_COL);
}

//==================================================================================
// --- 終了 ---
//==================================================================================
void UninitHappyEnd(void)
{

}

//==================================================================================
// --- 更新 ---
//==================================================================================
void UpdateHappyEnd(void)
{
	static float fRotate = 0.0f;
	fRotate -= 0.003f;

	fRotate = RepairRot(fRotate);

	SetRotation2DPolygon(g_nIdxHResultPolygon[1], fRotate);
}

