//**********************************************************************************
#define TEX_HAPPYEND		(2)		// ハッピーエンドの際に使うテクスチャ数	

//**********************************************************************************
//*** プロトタイプ宣言 ***
//**********************************************************************************

//**********************************************************************************
//*** グローバル変数 ***
